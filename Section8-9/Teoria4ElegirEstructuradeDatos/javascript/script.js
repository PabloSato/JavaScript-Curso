//ELEGIR ENTRE ARRAYs o SETs y MAPs o OBJECTs

//ARRAY vs SET
//Cuando no necesitamos describir los datos

/*ARRAY
Usamos Array cuando:
    - queremos tener la información ordenada dentro del conjunto
    - No nos importa que haya valores duplicados
    - Cuando necesitamos manipular los datos del conjunto

/*SET
Usamos Set cuando:
    - Queremos trabajar con valores UNICOS
    - Cuando el rendimiento es muy importante
    - Cuando no queremos los duplicados o queremos borrar los valores duplicados
      de un array

//MAP vs OBJECT
//Cuando necesitamos describir los datos

/*OBJECT
Usamos Objetos cuando:
    - Es la estructura llave-valor tradicional en programación
    - facil de prgramar y acceder a sus valores
    - Puedes guardar Funciones/Métodos como valores

/*MAP
Usamos Map cuando:
    - Mejor rendimiento
    - Las llaves pueden tener cualquier tipo de dato
    - Facilmente iterable
    - Facil computar su tamaño
*/
